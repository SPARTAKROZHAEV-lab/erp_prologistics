# Импортируем функцию create_app из пакета app (папка src/app)
from src.app import create_app

# Создаём экземпляр приложения, вызывая функцию-фабрику
app = create_app()

# Этот блок выполняется только если файл запущен напрямую (не импортирован)
if __name__ == '__main__':
    # Запускаем встроенный сервер Flask
    # debug=True – режим отладки (авто-перезагрузка при изменениях)
    # host='0.0.0.0' – слушаем все сетевые интерфейсы (доступно извне)
    # port=5000 – стандартный порт Flask
    app.run(debug=True, host='0.0.0.0', port=5000)